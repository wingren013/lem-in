#include "libft.h"

void	ft_putendl_fd(char const *s, int fd)
{
	if (s == 0)
	{
		write(fd, "\n", 1);
		return ;
	}
	ft_putstr_fd(s, fd);
	ft_putchar_fd('\n', fd);
}
